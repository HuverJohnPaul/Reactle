def reaction_maker(reaction_text,output_filename,output_dic_name):
    reactions = {}

    # split based off of new lines of a stripped of white space entry text, split into a list
    lines = reaction_text.strip().split('\n')
    
    # start line number at 0
    line_number = 0
    
    # go through each line in list and extract reactants and products
    for line in lines:
        #split the reaction based on the arrow
        reaction_components = line.split('->')
   
        #set the first and last parts to their appropriate reactents and products 
        reactants = reaction_components[0].strip()
        products = reaction_components[1].strip()
       
        # Split reactants and products into individual components
        reactants = [r.strip() for r in reactants.split('+')]
        products = [p.strip() for p in products.split('+')]

        # Create a reaction dictionary entry
        reaction_entry = {
           'reactants': reactants,
           'products': products
           }
        
        # Add the reaction entry to the reactions dictionary
        reaction_key = 'reaction' + str(line_number)
        reactions[reaction_key] = reaction_entry

        # add to line number to go to next line
        line_number += 1
    
    #make the newly made dictionary into a new file
    with open(output_filename, 'w') as output_file:
        
        # write a comment indicating a newly generated dictionary from this file
        output_file.write("# Newly generated dictionary made by reaction_maker\n")

        # write the dictionary name and an = sign
        output_file.write(output_dic_name + " = {\n")

        # write the dictionary using a loop going through the dictionary.items() adding each line's key and value
        for key, value in reactions.items():
            output_file.write("\t'" + key + "': " + str(value) + ",\n")
        #add the ending 
        output_file.write("}")
    print("done file made called:"+str(output_filename))


